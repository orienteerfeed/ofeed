#!/usr/bin/env bash
set -euo pipefail

RULES_FILE=".codex/rules/git.readonly.rules"
mkdir -p "$(dirname "$RULES_FILE")"

# 1:1 allow list (read-only)
allowed=(status log diff show rev-parse ls-files cat-file describe grep blame)

# (optional) mutating list for nicer messaging
mutating=(add commit push pull merge rebase reset checkout switch stash tag branch remote clean cherry-pick revert submodule)

# Get all available git subcommands
mapfile -t all < <(
  LC_ALL=C git help -a 2>/dev/null \
  | sed -n 's/^[[:space:]]\{2,\}\([a-z0-9][a-z0-9-]*\)[[:space:]].*$/\1/p' \
  | sort -u
)

declare -A allowset=()
for c in "${allowed[@]}"; do allowset["$c"]=1; done

declare -A mutset=()
for c in "${mutating[@]}"; do mutset["$c"]=1; done

mut_forbidden=()
other_forbidden=()

for c in "${all[@]}"; do
  # Forbid everything outside allowlist
  if [[ -z "${allowset[$c]+x}" ]]; then
    if [[ -n "${mutset[$c]+x}" ]]; then
      mut_forbidden+=("$c")
    else
      other_forbidden+=("$c")
    fi
  fi
done

starlark_union() {
  local first=1
  printf "["
  for s in "$@"; do
    if [[ $first -eq 0 ]]; then printf ","; fi
    first=0
    printf "\"%s\"" "$s"
  done
  printf "]"
}

{
  echo "# AUTO-GENERATED by .codex/bin/generate-git-readonly-rules.sh"
  echo "# Codex loads *.rules files and applies the MOST restrictive decision when multiple rules match."
  echo

  # Allow read-only
  echo "prefix_rule("
  echo "  pattern = [\"git\", $(starlark_union "${allowed[@]}")],"
  echo "  decision = \"allow\","
  echo "  justification = \"Allowed: read-only git commands.\","
  echo "  match = ["
  echo "    \"git status\","
  echo "    \"git log --oneline -n 5\","
  echo "    \"git diff --stat\","
  echo "  ],"
  echo ")"
  echo

  # Forbid mutating (nicer message)
  if [[ ${#mut_forbidden[@]} -gt 0 ]]; then
    echo "prefix_rule("
    echo "  pattern = [\"git\", $(starlark_union "${mut_forbidden[@]}")],"
    echo "  decision = \"forbidden\","
    echo "  justification = \"Blocked: mutating git command (do it manually).\","
    echo "  match = ["
    echo "    \"git commit -m test\","
    echo "    \"git push\","
    echo "  ],"
    echo ")"
    echo
  fi

  # Forbid everything else (read-only ONLY)
  if [[ ${#other_forbidden[@]} -gt 0 ]]; then
    echo "prefix_rule("
    echo "  pattern = [\"git\", $(starlark_union "${other_forbidden[@]}")],"
    echo "  decision = \"forbidden\","
    echo "  justification = \"Blocked: only the allowlisted read-only git subcommands are permitted.\","
    echo "  match = ["
    echo "    \"git fetch\","
    echo "    \"git restore .\","
    echo "    \"git help\","
    echo "  ],"
    echo ")"
    echo
  fi
} > "$RULES_FILE"

echo "Wrote: $RULES_FILE"
